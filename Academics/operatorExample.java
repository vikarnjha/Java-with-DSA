public class operatorExample {
    public static void main(String args[]) {
        int x = 10;
        // Post-increment:Print the current value of x(10), then increment x to 11
        System.out.println(x++); // 10(11)
        // Pre-increment: Print the new value of x(12)
        System.out.println(++x); // 12
        // Post-decrement:Print the current value of x(12), then decrement x to 11
        System.out.println(x--); // 12(11)
        // Pre-decrement: Print the new value of x(12)
        System.out.println(--x); // 10

    }
}
