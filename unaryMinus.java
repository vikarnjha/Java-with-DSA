public class unaryMinus {
    public static void main(String[] args) {
        // The unary minus operator - is used to negate a value, effectively changing its sign. For example, -5 is the negative of 5.

        int number = 10;
        int result = -number; // Apply unary minus operator

        System.out.println("The result of applying unary minus to " + number + " is: " + result);
        // Output:-
        // The result of applying unary minus to 10 is: -10
    }
}
