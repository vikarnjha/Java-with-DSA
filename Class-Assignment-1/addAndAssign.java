public class addAndAssign {
    public static void main(String[] args) {
        // What does x += 5 mean?
        // x += 5; increments x by 5, 

        int count = 10;   // Initialize count with a value of 10
        count += 3;       // Add 3 to count; count is now 13
        System.out.println(count); // Output will be 13
    }
}
